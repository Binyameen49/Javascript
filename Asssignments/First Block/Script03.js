// 3. Write a js program to check whether a number is negative, positive or
// zero.

let num01 = 50;

if (num01 > 0) {
    console.log(`${num01} is Positive Integer Because It Is Greater Than "0".`)
}

else if (num01 < 0) {
    console.log(`${num01} is Negative Integer Because It Is Less Than "0".`)
}

else {
    console.log(`${num01} is Zero.`)
}