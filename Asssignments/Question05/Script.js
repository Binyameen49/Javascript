let num01 = prompt("Enter A Number");

if (num01 % 2 === 0) {
    alert(`${num01} is an Even number.`);
    console.log(`${num01} is an Even number.`);
    document.write(`${num01} is an Even number.`);
}

else {
    alert(`${num01} is an Odd number.`);
    console.log(`${num01} is an Odd number.`);
    document.write(`${num01} is an Odd number.`);

}